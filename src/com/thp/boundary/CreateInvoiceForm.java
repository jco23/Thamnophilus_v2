    public boolean entryIsValid(javax.swing.JTextField txtFld)
    {
            boolean valid = true;
            try{
                Integer.parseInt(txtFld.getText());
                return valid;
            }
            catch(Exception e){
                txtFld.setBackground(Color.pink);
                txtFld.setText("Invalid entry");
                valid = false;
                return valid;
            }
    }
    public boolean doubleEntryIsValid(javax.swing.JTextField txtFld)
    {
            boolean valid = true;
            try{
                Double.parseDouble(txtFld.getText());
                return valid;
            }
            catch(Exception e){
                txtFld.setBackground(Color.pink);
                txtFld.setText("Invalid entry");
                valid = false;
                return valid;
            }
    }
    public boolean isValidDate(javax.swing.JTextField txtFld)
    {
        boolean valid = true;
            try{
                Date.valueOf(txtFld.getText());
                return valid;
            }
            catch(Exception e){
                txtFld.setBackground(Color.pink);
                valid = false;
                return valid;
            }
    }
    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        jFrame1.setVisible(true);
        try {
                jListModel.removeAllElements();
                 int i = 1;
                 Statement stmt = AccountDB.conn.createStatement();
                 String sql = "SELECT ID, WIDGETNAME, QTY, CUNITPRICE FROM APP.WIDGETS";
                 ResultSet rs = stmt.executeQuery(sql);
                 jListModel .add(0, "  <ID>     <Widget>     <Qty>     <Price>");
                 while(rs.next()){
                      String id = rs.getString("ID");      
                      String wname = rs.getString("WIDGETNAME");
                      String qty = rs.getString("QTY");
                      String uprice = rs.getString("CUNITPRICE");
                      jListModel.add(i, "      " + id + "      " + wname + "      " + qty + "      " + uprice + "      ");
                      i++;
                 }    
        } 
        catch (SQLException ex) {
        }
    }//GEN-LAST:event_jButton3MouseClicked
    
