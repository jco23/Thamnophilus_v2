    private void jCreateBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jCreateBtnMouseClicked
        // TODO add your handling code here:
        //pass each data item into the database
        String clean = this.jPhoneTxt.getText().replaceAll("\\(", "");
        clean = clean.replaceAll("\\)","");
        clean = clean.replaceAll("-","");
        clean = clean.replaceAll(" ", "");

        if(this.jFirstNameTxt.getText().isEmpty() || 
           this.jFirstNameTxt.getText().length() > 30 ||
           this.jLastNameTxt.getText().isEmpty() || 
           this.jLastNameTxt.getText().length() > 30 || 
           clean.length() != 10){
        
            if(this.jFirstNameTxt.getText().isEmpty()){
                this.jFirstNameTxt.requestFocus();
                this.jFirstNameTxt.setText("");
                this.jFirstNameTxt.setBackground(Color.pink);
                this.jFirstNameStatusMsg.setVisible(true);
                this.jFirstNameStatusMsg.setText("First name is required.");

            }
            else if(this.jFirstNameTxt.getText().length() > 30){
                this.jFirstNameTxt.requestFocus();
                this.jFirstNameTxt.setBackground(Color.pink);
                this.jFirstNameStatusMsg.setVisible(true);
                this.jFirstNameStatusMsg.setText("First name exceeds 30 characters.");
            }
            if(this.jLastNameTxt.getText().isEmpty()){
                this.jLastNameTxt.requestFocus();
                this.jLastNameTxt.setText("");
                this.jLastNameTxt.setBackground(Color.pink);
                this.jLastNameStatusMsg.setVisible(true);
                this.jLastNameStatusMsg.setText("Last name is required.");
            }
            else if(this.jLastNameTxt.getText().length() > 30){
                this.jLastNameTxt.requestFocus();
                this.jLastNameTxt.setBackground(Color.pink);
                this.jLastNameStatusMsg.setVisible(true);
                this.jLastNameStatusMsg.setText("Last name exceeds 30 characters");
            }
            if(clean.length() == 0)
            {
                this.jPhoneTxt.requestFocus();
                this.jPhoneTxt.setText("");
                this.jPhoneTxt.setBackground(Color.pink);
                this.jPhoneStatusMsg.setVisible(true);
                this.jPhoneStatusMsg.setText("Phone number is required.");
            }
            else if(clean.length() < 10){
                this.jPhoneTxt.requestFocus();
                this.jPhoneTxt.setText("");
                this.jPhoneTxt.setBackground(Color.pink);
                this.jPhoneStatusMsg.setVisible(true);
                this.jPhoneStatusMsg.setText("Phone number is not 10 digits long");
            }
        }
        else{ 
            Salesperson salesperson = new Salesperson();
            salesperson.setPerson(this.jFirstNameTxt.getText().toUpperCase(), this.jLastNameTxt.getText().toUpperCase());
            salesperson.setPhone(this.jPhoneTxt.getText());
            String msg = SalespersonControl.createSalesperson(salesperson);

            this.jStatusMsg.setText(msg);
            this.jStatusMsg.setVisible(true);
            this.jStatusMsg.setForeground(Color.BLUE);
            resetTextFields();
        }
    }//GEN-LAST:event_jCreateBtnMouseClicked
    public void resetTextFields() {
        this.jFirstNameTxt.setText("");
        this.jLastNameTxt.setText("");
        this.jPhoneTxt.setText("");
   }
